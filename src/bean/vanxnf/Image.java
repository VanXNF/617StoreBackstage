package bean.vanxnf;

/**单张图片属性 - 表image*/
public class Image {
    /**图片id*/
    private int id;
    /**图片链接*/
    private String url;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
