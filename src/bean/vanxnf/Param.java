package bean.vanxnf;

/**单条参数 - 表parameter*/
public class Param {
    /**参数id*/
    private int id;
    /**参数值*/
    private String content;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
